function onDeviceReady() {	
	
	$('#flipCurrentLocation').bind("change", function() {
		var sliderLocationVal = $('#flipCurrentLocation').val();

		if (sliderLocationVal == 'yes') {
			$('#location').prop('disabled', true);
			$('#location').css({
				'background-color' : 'grey',
			});

			navigator.geolocation.getCurrentPosition(onSuccess, onError, { enableHighAccuracy: true });
		} else {
			$('#location').prop('disabled', false);
			$('#location').css({
				'background-color' : 'white',
			});
		}
	});
	
	$('#location').bind("change", function() {
		console.log('Location changed!');
	});

	$('#createEventButton').click(function() {
		
		var usersInvited = new Array();
		
		var usersArray = $('.userCheckBox');
		$.each(usersArray, function(i, item){
			console.log("Checked: " + usersArray.eq(i).is(':checked'));
			if (usersArray.eq(i).is(':checked') == true) {
				usersInvited.push({id : usersArray.eq(i).attr('value')});
			}
		});
		
		console.log("SOCIALIZE - ZACETEK");

		console.log('Trenutno prijavljen uporabnik: ' + window.sessionStorage.getItem("userId"));

		var des = $('#eventDescription').val();
		var isPublicEvent = ($('#flipPublicEvent').val() == 'yes') ? 1 : 0;
		
		var data = JSON.stringify({
			id : -1,
			date : new Date(),
			description : des + '',
			lat :  dogodek.pos.lat + '',
			lon :  dogodek.pos.lon + '',
			publicEvent : isPublicEvent,
			user : {id : window.sessionStorage.getItem("userId")},
			//invited: JSON.stringify(usersInvited),
		});
		
		console.log("Poslani podatki: "  + data);
		
		$.ajax({
			type : "POST",
			contentType : "application/json",
//			url : "http://192.168.1.2:8080/RateABeer_web/rest/user/login",
			url : restUrl + "event/create/",
			processData : false,
			data : data,
			success : function(data) { 

				console.log('Klic uspesen.');
				console.log(JSON.stringify(data));
				
				console.log("Ustvarjen dogodek z ID: " + data.idEvent);
				
				if (data.result === 'true') {
					console.log('Users invited: ' + JSON.stringify(usersInvited));
					
					$.each(usersInvited, function(i, item) {
						$.ajax({
							type : "POST",
							contentType : "application/json",
							url : restUrl + "event/create/"+data.idEvent+"/invite",
							processData : false,
							data : JSON.stringify(usersInvited[i]),
							success : function(dataInvited) { 

								console.log('Klic uspesen: ' + JSON.stringify(item));
								console.log(JSON.stringify(dataInvited));

							  },
							  error       : function(xhr, textStatus, errorThrown){ 
								  console.log(textStatus);
								  console.log(JSON.stringify(xhr));
							  }
						});
					});
					
					console.log("REZULTAT USPESNO");
					navigator.notification.alert(
						    'The event has been successfully added.',  // message
						    function() {window.location.replace('socialize.html');}, // callback
						    'New event', // title
						    'Close' // buttonName
						);
				} else {
					console.log("REZULTAT NEUSPESNO");
				}	
				
			  },
			  error       : function(xhr, textStatus, errorThrown){ 
				  console.log("NAPAKA PRI KLICU USTVARJANJA DOGODKA");
				  console.log(textStatus);
				  console.log(JSON.stringify(xhr));
				  
				  navigator.notification.alert(
						    'There has been an error creating the event. Please try again later.',  // message
						    function() {}, // callback
						    'New event', // title
						    'Close' // buttonName
						);
			  }
		});
		
		console.log("SOCIALIZE - KONEC");
	});
}

function onSuccess(position) {
    // Shranimo pozicijo
    dogodek.pos = {lon : position.coords.longitude, lat : position.coords.latitude};
    
    console.log("Pridobljena pozicija: " + JSON.stringify(dogodek.pos));
}

function onError(error) {
	var errorMessage = 'code: '    + error.code    + '\n' +
    					'message: ' + error.message + '\n';
	
    console.log(errorMessage);
	alert(errorMessage);
}

function fetchUsers() {
	$.ajax({
		type : "GET",
		url : restUrl + "user/users/",
		dataType : "json",
		success : function(data) {

			var jsonData = (data.user instanceof Array ? data.user : data);
			var listItem = "";
			$.each(jsonData, function(i, item) {
//				console.log("Uporabnik: " + item.username);
				
				listItem = "<input type='checkbox' name='checkbox-"+item.id+"' id='checkbox-mini-"+item.id+"' value='"+item.id+"' class='custom userCheckBox' data-mini='true' /><label for='checkbox-mini-"+item.id+"'>"+item.username+"</label>";
				
				$('#usersList').prepend("<li>"+listItem+"</li>");

				$('#usersList').trigger('create');
				$("#usersList").listview('refresh');
			});
			$('#usersList').trigger('create');
			$("#usersList").listview('refresh');
		},
		error : function(xhr, textStatus, errorThrown) {
			alert("Napaka: " + xhr + " " + xhr.status);
			alert(xhr.responseText);
		}
	});
}

